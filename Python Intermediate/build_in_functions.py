# Math Functions
A = 30
B = 45
C = 36

#Output the maximum value of A, B, C
print(max(A, B, C))

#Output the minimum value of A, B, C
print(min(A, B, C))

#Output the absolute value of the difference of A and C
print(abs(A - C))

